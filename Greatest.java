public class Greatest {
    public static void main(String[] args) {
        int a = 12, b = 13;
        if (a > b) {
            System.out.println(a + "is greatest");
        }
        else if (b > a) {
            System.out.println(b + "is greatest");
        }
        else{
            System.out.println("Both are equal");
        }
    }
}

